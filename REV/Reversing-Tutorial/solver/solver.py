def rol(value, shift):
    return ((value << shift) & 0xFF) | (value >> (8 - shift))

def ror(value, shift):
    return (value >> shift) | ((value << (8 - shift)) & 0xFF)

transformed = [0x3C, 0x2C, 0x02, 0x26, 0x14, 0x3E, 0x4E, 0x5C, 0xCC, 0x68, 0x4C, 0x18, 0x14, 0x0E, 0xCC, 0x46, 0xCC, 0x4E, 0xCC, 0xC8, 0x76, 0x24, 0x14, 0x66, 0x00, 0x76, 0x36, 0x58, 0x14, 0xCC, 0x36, 0x7E, 0xCA, 0x18, 0x50]

flag = []

for value in transformed:
    c = rol(value, 2)
    c ^= 0xAA
    c = ror(c, 3)
    flag.append(c)

flag_str = ''.join(chr(x) for x in flag)
print(flag_str)

